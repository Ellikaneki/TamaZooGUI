    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (txtNomeTama.getText().isBlank()) {
            FinestraMessaggio errore = new FinestraMessaggio(padre, true);
            errore.setTitolo("Errore!");
            errore.setMessaggio("Il nome non può essere vuoto");
            errore.setIcona("src/it/iis/tama/assets/TamaTriste32.png");
            errore.setVisible(true);
            return;
        }
        padre.aggiungiTama(creaTama());
        dispose();
    }//GEN-LAST:event_btnOkActionPerformed

    private Tamagotchi creaTama() {
        String nome = txtNomeTama.getText();
        int affettoIniziale = sliderAffetto.getValue();
        int sazietaIniziale = sliderSazieta.getValue();
        return switch (tipoTamaAttuale) {
            case TIPO_TRISTE ->
                new TamaTriste(nome, sazietaIniziale);
            case TIPO_GORDO ->
                new TamaGordo(nome, sazietaIniziale);
            default ->
                new Tamagotchi(nome, affettoIniziale, sazietaIniziale);
        };
    }

