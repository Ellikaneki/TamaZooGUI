    private void btnSuccessivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuccessivoActionPerformed
        tipoTamaAttuale++;
        btnPrecedente.setEnabled(true);
        if (tipoTamaAttuale == immagini.length - 1) {
            btnSuccessivo.setEnabled(false);
        }
        aggiornaImmagine();
        aggiornaTipoTama();
        aggiornaAffetto();
    }//GEN-LAST:event_btnSuccessivoActionPerformed

    private void aggiornaImmagine() {
        iconaTama.setIcon(new ImageIcon(immagini[tipoTamaAttuale]));
    }

    private void aggiornaTipoTama() {
        switch (tipoTamaAttuale) {
            case TIPO_TRISTE:
                lblTipoTama.setText("TAMATRISTE");
                break;
            case TIPO_GORDO:
                lblTipoTama.setText("TAMAGORDO");
                break;
            default:
                lblTipoTama.setText("TAMAGOTCHI");
                break;
        }
    }

    private void aggiornaAffetto() {
        switch (tipoTamaAttuale) {
            case TIPO_TRISTE:
                sliderAffetto.setMinimum(Tamagotchi.MIN_AFFETTO);
                sliderAffetto.setValue(Tamagotchi.MIN_AFFETTO);
                lblAffetto.setText(String.valueOf(Tamagotchi.MIN_AFFETTO));
                sliderAffetto.setEnabled(false);
                break;
            case TIPO_GORDO:
                sliderAffetto.setValue(Tamagotchi.MAX_AFFETTO);
                lblAffetto.setText(String.valueOf(Tamagotchi.MAX_AFFETTO));
                sliderAffetto.setEnabled(false);
                break;
            default:
                sliderAffetto.setValue(50);
                lblAffetto.setText("50");
                sliderAffetto.setEnabled(true);
                break;
        }
    }

    private void btnPrecedenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrecedenteActionPerformed
        tipoTamaAttuale--;
        btnSuccessivo.setEnabled(true);
        if (tipoTamaAttuale == 0) {
            btnPrecedente.setEnabled(false);
        }
        aggiornaImmagine();
        aggiornaTipoTama();
        aggiornaAffetto();
    }//GEN-LAST:event_btnPrecedenteActionPerformed

